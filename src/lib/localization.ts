function isLikelyValidKorean(value: string) {
  return /[가-힣]/.test(value) && !/[?]{2,}|占/.test(value);
}

function translateSnakeCase(raw: string) {
  if (!/^[a-z0-9_]+$/.test(raw) || !raw.includes("_")) {
    return null;
  }

  const translated = raw
    .split("_")
    .map((token) => KO_SNAKE_CASE_TOKEN_MAP[token] ?? token.toUpperCase())
    .join(" ");

  return translated === raw ? null : translated;
}

export function localizeText(locale: "ko" | "en", text?: string) {
  const raw = String(text ?? "");
  if (locale !== "ko" || !raw.trim()) {
    return raw;
  }

  const exact = KO_EXACT[raw];
  if (exact) {
    return exact;
  }

  const snakeCase = translateSnakeCase(raw);
  if (snakeCase) {
    return snakeCase;
  }

  for (const entry of KO_PATTERNS) {
    const matches = raw.match(entry.pattern);
    if (matches) {
      return entry.translate(matches);
    }
  }

  return raw;
}

export function localizeTextWithFallback(locale: "ko" | "en", ko: string, en: string) {
  if (locale !== "ko") {
    return en;
  }

  const translated = localizeText("ko", en);
  if (translated !== en) {
    return translated;
  }

  if (isLikelyValidKorean(ko)) {
    return ko;
  }

  return en;
}
